export function getWriterSystemPrompt(genre: string, chapterNumber: number): string {
  // 장르 매칭 (부분 매칭)
  let prompt = WRITER_SYSTEM_PROMPTS.default;

  for (const [key, value] of Object.entries(WRITER_SYSTEM_PROMPTS)) {
    if (key !== "default" && genre.includes(key)) {
      prompt = value;
      break;
    }
  }

  // 로판 계열 통합
  if (genre.includes("로맨스") && genre.includes("판타지")) {
    prompt = WRITER_SYSTEM_PROMPTS["로맨스 판타지"];
  }
  if (genre.includes("빙의")) {
    prompt = WRITER_SYSTEM_PROMPTS["로맨스 판타지"];
  }

  return prompt + UNIVERSAL_RULES_SUFFIX;
}

