export function detectGenre(text: string): string {
  if (text.includes("빙의")) return "로맨스 빙의물";
  if (text.includes("로판") || text.includes("로맨스 판타지")) return "로맨스 판타지";
  if (text.includes("현대 로맨스") || text.includes("현로")) return "현대 로맨스";
  if (text.includes("로맨스")) return "현대 로맨스";
  if (text.includes("정통 판타지")) return "정통 판타지";
  if (text.includes("무협")) return "무협";
  if (text.includes("회귀") || text.includes("귀환")) return "회귀";
  return "현대 판타지";
}
